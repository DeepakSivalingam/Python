import math

# math functions in python 
print(math.sqrt(9))
print(math.floor(1.9))
print(math.ceil(1.3))
print(math.factorial(9))
print(math.fabs(-9))
print(math.pow(9,2))
print(math.pi)
print(math.e)
print(math.fmod(10, 3)) 

print(math.gcd(24, 36))      # Greatest common divisor → 12
print(math.lcm(4, 6))        # Least common multiple → 12 (Python 3.9+)


